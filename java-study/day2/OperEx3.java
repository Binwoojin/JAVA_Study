public class OperEx3 {
    public static void main(String[] args) {
        byte b = 10;
        System.out.println("b >> 2 : " + (b >> 2));
        System.out.println("b << 2 : " + (b << 2));
        System.out.println("----------------------");
        System.out.println("b >> 3 : " + (b >> 3));
        System.out.println("b >> 3 : " + (b << 3));

    }
    
}
