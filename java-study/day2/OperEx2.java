public class OperEx2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("a + b = "+ (a+b));
        System.out.println("a - b = "+ (a-b));
        System.out.println("a * b = "+ (a*b));
        System.out.println("a / b = "+ (a/b));
        System.out.println("a % b = "+ (a%b));


        byte m1 = 100;
        short m2 = 15;

        System.out.println("m1 / m2 = " + (m1/m2));
    }
    
}
