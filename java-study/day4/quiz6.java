public class quiz6 {
    public static void main(String[] args) {
        // for문을 사용하여 0부터 10 미만의 정수의 합을 출력
        int total = 0; // 합계를 저장할 변수 선언
        for(int i = 0; i < 10; i++) { // 0부터 10 미만까지 반복
            total += i; // total = total + i
        }
        System.out.println(total); // 최종 합계 출력
    }    

}
